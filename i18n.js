// i18n System
// ============================================
(function () {
    let currentLang = localStorage.getItem('hssc-lang') || 'es';

    function applyTranslations(lang) {
        const dict = translations[lang];
        if (!dict) return;

        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            const key = el.getAttribute('data-i18n');
            if (dict[key] !== undefined) {
                el.innerHTML = dict[key];
            }
        });

        // Handle placeholder translations
        document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
            const key = el.getAttribute('data-i18n-placeholder');
            if (dict[key] !== undefined) {
                el.setAttribute('placeholder', dict[key]);
            }
        });

        // Update html lang attribute
        document.documentElement.lang = lang;

        // Update toggle button text
        const toggleBtns = document.querySelectorAll('.lang-toggle span');
        toggleBtns.forEach(function (span) {
            span.textContent = lang === 'es' ? 'EN' : 'ES';
        });

        currentLang = lang;
        localStorage.setItem('hssc-lang', lang);
    }

    // Initialize on DOM ready
    document.addEventListener('DOMContentLoaded', function () {
        // Always apply translations on load (to cover both ES re-apply and EN switch)
        applyTranslations(currentLang);

        // Bind toggle buttons
        document.querySelectorAll('.lang-toggle').forEach(function (btn) {
            btn.addEventListener('click', function () {
                const newLang = currentLang === 'es' ? 'en' : 'es';
                applyTranslations(newLang);
            });
        });
    });
})();
